package com.szdeepwise.bydtcmonitor.entity;

public class SceneListEntity {
    private Integer sceneId;
    private String sceneName;//场景英文名
    private String sceneTableName;//scene_table_name
    private String sceneNameDesc;//场景中文名

    public String getSceneTableName() {
        return sceneTableName;
    }

    public void setSceneTableName(String sceneTableName) {
        this.sceneTableName = sceneTableName;
    }

    public Integer getSceneId() {
        return sceneId;
    }

    public void setSceneId(Integer sceneId) {
        this.sceneId = sceneId;
    }

    public String getSceneName() {
        return sceneName;
    }

    public void setSceneName(String sceneName) {
        this.sceneName = sceneName;
    }

    public String getSceneNameDesc() {
        return sceneNameDesc;
    }

    public void setSceneNameDesc(String sceneNameDesc) {
        this.sceneNameDesc = sceneNameDesc;
    }

    @Override
    public String toString() {
        return "SceneListEntity{" +
                "sceneId=" + sceneId +
                ", sceneName='" + sceneName + '\'' +
                ", sceneTableName='" + sceneTableName + '\'' +
                ", sceneNameDesc='" + sceneNameDesc + '\'' +
                '}';
    }
}
